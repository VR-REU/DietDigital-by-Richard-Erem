    public virtual int GetCalorieNeeds(int age, Gender gender, ActivityLevel activityLevel)
    {
        if (age < 2 || age > 76)
        {
            Debug.LogError("Age out of range");
            return 0;
        }

        switch (gender)
        {
            case Gender.Male:
                switch (activityLevel)
                {
                    case ActivityLevel.Sedentary:
                        return maleSedentary[age];
                    case ActivityLevel.ModeratelyActive:
                        return maleModeratelyActive[age];
                    case ActivityLevel.Active:
                        return maleActive[age];
                    default:
                        Debug.LogError("Invalid activity level");
                        return 0;
                }
            case Gender.Female:
                switch (activityLevel)
                {
                    case ActivityLevel.Sedentary:
                        return femaleSedentary[age];
                    case ActivityLevel.ModeratelyActive:
                        return femaleModeratelyActive[age];
                    case ActivityLevel.Active:
                        return femaleActive[age];
                    default:
                        Debug.LogError("Invalid activity level");
                        return 0;
                }
            default:
                Debug.LogError("Invalid gender");
                return 0;
        }
    }
